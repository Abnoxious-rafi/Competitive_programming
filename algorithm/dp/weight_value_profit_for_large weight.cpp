ll N = 105, n, w, inf = 1e12 + 9;

vector<ll> weight(N), value(N);
ll dp[105][105000];

ll kuddor_kuddor_dfs(int index, ll total_value)
{
     if (index > n)
     {
          if (total_value == 0)
               return 0;
          return inf;
     }
     if (dp[index][total_value] != -1)
          return dp[index][total_value];
     ll ans = inf;
     ans = kuddor_kuddor_dfs(index + 1, total_value);

     ans = min(ans, weight[index] + kuddor_kuddor_dfs(index + 1, total_value - value[index]));
     return dp[index][total_value] = ans;
}
